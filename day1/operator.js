// Arithmetic Operators
let a = 10;
let b = 3;

console.log("Arithmetic Operators:");

console.log("Addition:", a + b); // Output: 13
console.log("Sub:", a - b); // Output: 7
console.log("Mul:", a * b); // Output: 30
console.log("Division:", a / b); // Output: 3.333...
console.log("Modulus:", a % b); // Output: 1

// Assignment Operators
let c = 5;
console.log("\n\n\nAssignment Operators:");

c += 2; // c = c + 2;
console.log("Addition Assignment:", c); // Output: 7
c -= 2; // c = c - 2;
console.log("Subtraction Assignment:", c); // Output: 5
c *= 2; // c = c * 2;
console.log("Multiplication Assignment:", c); // Output: 10
c /= 2; // c = c / 2;
console.log("Division Assignment:", c); // Output: 5
c %= 2; // c = c % 2;
console.log("Modulus Assignment:", c); // Output: 1

// Comparison Operators
let x = 10;
let y = 5;
console.log("\n\n\nComparison Operators:");
console.log("Equal:", x == y); // Output: false
console.log("Not Equal:", x != y); // Output: true
console.log("Strict Equal:", x === y); // Output: false
console.log("Strict Not Equal:", x !== y); // Output: true
console.log("Greater Than:", x > y); // Output: true
console.log("Less Than:", x < y); // Output: false
console.log("Greater Than or Equal:", x >= y); // Output: true
console.log("Less Than or Equal:", x <= y); // Output: false

// Logical Operators
let p = true;
let q = false;

console.log("\nLogical Operators:");
console.log("AND:", p && q); // Output: false
console.log("OR:", p || q); // Output: true
console.log("NOT p:", !p); // Output: false
console.log("NOT q:", !q); // Output: true
